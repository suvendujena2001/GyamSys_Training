Where,
Console: It's a class in the System namespace that provides methods for interacting with the console, which is the text-based interface in which a program receives input and displays output.

WriteLine: It's a method of the Console class used to display a line of text on the console. The WriteLine method automatically appends a newline character (\n) after the specified text, so the next output will be on a new line.

"Hello, World!": This is the string that will be displayed on the console. In this case, it's a simple greeting message.
